Clock::Clock(): App() {
	// Keep the constructor empty and do most things in the init()
}
void Clock::init() {
	m_noise = false;
	m_icon = new Icon(28,myicon);
	m_timeZone = configuration.getTimeZone();
}

void Clock::setup() {
	Graphics.fillScreen(BLACK);
	Graphics.setRotation(3);
	m_buttonOn->draw();
	m_buttonOff->draw();
	m_buttonMars->draw();
}

void Clock::close() {
	m_noise = false;
}

boolean Clock::touch(TS_Point p) {
	if (m_buttonOn->isClicked(p)) {
		m_buttonOn->flash();
		m_noise = false;
	}
	else if (m_buttonOff->isClicked(p)) {
		m_buttonOff->flash();
		m_noise = true;
	}
	else if (m_buttonMars->isClicked(p)) {
		m_buttonMars->flash();
		m_mars = !m_mars;
	}
	return true;
}
void Clock::display() {
	if (m_noise) {
		Hardware.playTone(3136,30,40);
		Hardware.playTone(4186,30,9);
		Hardware.blink1();
	}
	Graphics.fillRect(0,0,Graphics.width(),120,BLACK);
	Graphics.setCursor(60,50);
	Graphics.setTextColor(WHITE);
	Graphics.setTextSize(3);
	Graphics.setFont(Arial_20);

	long adjustedDate = now();
	if (m_mars) {
		// display mars time
		float JDUT = 2440587.5+(adjustedDate/SECONDS_PER_DAY);
		float JDTT = JDUT +(37+32.184)/86400;
		float deltaTJ2000 = JDTT - 2451545.0;
		float msd = (((deltaTJ2000 - 4.5)/1.027491252)+44796.0-0.00096);

		int mtcHours = ((int)(24 * msd)) % 24;
		int mtcMins = ((int)(24*60*msd)) % 60;
		int mtcSeconds = ((int)(24*60*60*msd)) % 60;
		Graphics.print(mtcHours);
		Graphics.print(":");
		Graphics.print(mtcMins);
		Graphics.print(":");
		Graphics.print(mtcSeconds);
		Graphics.setCursor(50,100);
		Graphics.println(msd);
		Graphics.setFont(Hardware.getDefaultFont());
	} else {
		// Earth time
		Graphics.println(Hardware.timeString(adjustedDate));
		Graphics.setCursor(50,100);
		Graphics.println(Hardware.dateString(adjustedDate));
		Graphics.setFont(Hardware.getDefaultFont());
	}
}

Clock::~Clock() {
}

