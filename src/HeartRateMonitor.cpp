void HeartRateMonitor::initaliseGraph(int bpm, float interval) {
	Poincare *poincare = heartRateInterrupt.getPoincare();
	int maxPoincare = poincare->getMax();

#ifdef HEARTRATEMONITOR_DEBUG
	Serial.print(getName());
	Serial.println("::initaliseGraph");
	Serial.print(" maxPoincare ");
	Serial.print(maxPoincare);
	Serial.print(" m_intervals ");
	for (int i=0;i<maxPoincare;i++) {
		Serial.print(poincare->get(i));
		Serial.print(",");
}

//	Serial.print(" m_lastMin ");
//	Serial.print(m_lastMin);
//	Serial.print(" m_lastMax ");
//	Serial.print(m_lastMax);
//	Serial.print(" bpm ");
//	Serial.print(bpm);
//	Serial.print(" m_lastScale ");
//	Serial.print(m_lastScale);
	Serial.println();
#endif
	Graphics.fillRect(0,0,150,100,WHITE);
	Graphics.fillRect(0,100,Graphics.width(),Graphics.height()-100,WHITE);
	Graphics.setCursor(10,20);
	uint16_t colour = GREEN;
	if (bpm < configuration.getLowPulse()) {
		colour = BLUE;
	}
	if (bpm > configuration.getHighPulse()) {
		colour = RED;
	}
	Graphics.setTextColor(colour);
	Graphics.setTextSize(2);
	Graphics.print("BPM: ");
	Graphics.print(bpm);
	Graphics.setCursor(10,40);
	Graphics.print("INT: ");
	if (interval > 0 && interval < 10000) {
		int i = interval;
		Graphics.print(i);
	}
	Graphics.drawRect(160, 0, 160, 100, BLACK);
	for (int i=1;i<maxPoincare;i++) {
		float i1 = poincare->get(i-1);
		float i2 = poincare->get(i);
		volatile int y = (i1-800)*0.04;
		volatile int x = 160+((i2-800)*0.025);
		long key = (x*10000)+y;
		int colour = BLACK;
		if (key > 0) {
			int frequency = m_map->put(key);
			if (frequency < 20) {
				colour = BLUE;
			} else if (frequency < 50) {
				colour = GREEN;
			} else if (frequency < 80) {
				colour = YELLOW;
			} else {
				colour = RED;
			}
		}
		Graphics.fillCircle(constrain(x,0,320),constrain(y,0,240),2,colour);
	}

}

